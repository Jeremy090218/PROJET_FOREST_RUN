class Element {

  /////// Constructeur D'element avec x y et z par default a 0 ///////////
  constructor(x=0,y=0,z=0) {
                                    // Pour donner un id différent à chaques éléments
    if(!Element.id) Element.id = 0;
    ++Element.id;
    this.x = x;
    this.y = y;
    this.z = z;
  }

////////////////////////////////////////////////////////////////////////////////
////////////////////////  GETTERS  /////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
  getId(){return this.id;}
  getX(){return this.x;}
  getY(){return this.y;}
  getZ(){return this.z;}
////////////////////////////////////////////////////////////////////////////////
////////////////////////  SETTERS  /////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
  setId(i){this.id = i;}
  setX(i){this.x = i;}
  setY(i){this.y = i;}
  setZ(i){this.z = i;}
}
