class Utilisateur {
  constructor(personnage,atelier) {
    this.personnage = personnage;
    this.atelier = atelier ;
  }


////////////////////////////////////////////////////////////////////////////////
///////////////////////// GETTERS //////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

  getPersonnage(){return this.personnage;}
  getAtelier(){return this.atelier;}

////////////////////////////////////////////////////////////////////////////////
///////////////////////// SETTERS //////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

  setPersonnage(i){this.personnage = i ;}
  setAtelier(i){this.atelier = i ;}
}
