class Trajectoire {
  constructor(points = 0 , vitesse = 0) {
    this.points = points ;
    this.vitesse = vitesse;
  }

////////////////////////////////////////////////////////////////////////////////
/////////////////////// GETTERS ////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////
  getPoints(){return this.points;}
  getVitesse(){return this.vitesse;}
/////////////////////////////////////////////////////////////////////////////////
/////////////////////// SETTERS ////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
  setPoints(i){this.points = i;}
  setVitesse(i){this.vitesse = i;}


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
  estLibre(){
    return true;
  }
}
