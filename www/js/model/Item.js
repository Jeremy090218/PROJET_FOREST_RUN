class Item {
  constructor(nom,achat,equipe) {
    this.nom = nom;
    this.achat = achat;
    this.equiper = equipe;
  }

////////////////////////////////////////////////////////////////////////////////
///////////////////// GETTERS //////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

  getNom(){return this.nom ; }
  getAchat(){return this.achat ; }
  getEquiper(){return this.equiper ; }

////////////////////////////////////////////////////////////////////////////////
//////////////////////////SETTERS //////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

  setNom(i){this.nom = i ;}
  setAchat(i){this.achat = i ;}
  setEquiper(i){this.Equiper = i;}



////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

  achatItem(){
    this.setAchat(true);
  }
  equiperItem(){
    this.setEquiper(true);
  }
  desequiperItem(){
    this.setEquiper(false);
  }
}
